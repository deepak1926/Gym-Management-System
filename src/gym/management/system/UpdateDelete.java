    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(txtId.getText());
        try {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            String query = "select * from members where id= " + id;
            ResultSet rs = st.executeQuery(query);
            while (rs.next()) {
                if (id == rs.getInt(1)) {
                    txtName.setText(rs.getString(2));
                    txtMobileNumber.setText(rs.getString(3));
                    txtEmail.setText(rs.getString(4));
                    txtFatherName.setText(rs.getString(6));
                    txtGender.setText(rs.getString(5));
                    txtMotherName.setText(rs.getString(7));
                    txtGymTime.setText(rs.getString(8));
                    txtAadharNumber.setText(rs.getString(10));
                    txtAge.setText(rs.getString(9));
                    txtAmountMonth.setText(rs.getString(11));
                    setVisiblity();
                } else {
                    JOptionPane.showMessageDialog(null, "<html><b style=\"color:orange;\">Memeber does  not exits</b></html>");
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnSearchActionPerformed

