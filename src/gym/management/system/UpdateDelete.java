    public void validFields() {
        String name = txtName.getText();
        String mobile = txtMobileNumber.getText();
        String email = txtEmail.getText();
        String gender = txtGender.getText();
        String father = txtFatherName.getText();
        String mother = txtMotherName.getText();
        String gymTime = txtGymTime.getText();
        String age = txtAge.getText();
        String aadhar = txtAadharNumber.getText();
        String amount = txtAmountMonth.getText();
        if (!name.equals("") && !mobile.equals("") && !email.equals("") && !gender.equals("") && !father.equals("") && !mother.equals("") && !gymTime.equals("") && !age.equals("") && !aadhar.equals("") && !amount.equals("")) {
            btnUpdate.setEnabled(true);
        } else {
            btnUpdate.setEnabled(false);
        }
    }

    public void setVisiblity() {
        if (txtId.getText().equals("")) {
            txtName.setEnabled(false);
            txtMobileNumber.setEnabled(false);
            txtEmail.setEnabled(false);
            txtGender.setEnabled(false);
            txtFatherName.setEnabled(false);
            txtMotherName.setEnabled(false);
            txtGymTime.setEnabled(false);
            txtAadharNumber.setEnabled(false);
            txtAge.setEnabled(false);
            txtAmountMonth.setEnabled(false);
            btnUpdate.setEnabled(false);
            btnDelete.setEnabled(false);
            txtId.setEnabled(true);
            btnSearch.setEnabled(true);
        } else {
            txtId.setEnabled(false);
            txtName.setEnabled(true);
            txtMobileNumber.setEnabled(true);
            txtEmail.setEnabled(true);
            txtGender.setEnabled(true);
            txtFatherName.setEnabled(true);
            txtMotherName.setEnabled(true);
            txtGymTime.setEnabled(true);
            txtAadharNumber.setEnabled(true);
            txtAge.setEnabled(true);
            txtAmountMonth.setEnabled(true);
            btnUpdate.setEnabled(true);
            btnDelete.setEnabled(true);
            btnSearch.setEnabled(false);
        }
    }

