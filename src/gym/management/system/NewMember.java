    public void validField() {
String id = txtId.getText();
String name = txtName.getText();
        String mobile = txtMobileNumber.getText();
        String email = txtEmail.getText();
        String fatherName = txtFatherName.getText();
        String motherName = txtMotherName.getText();
        String aadhar = txtAadhar.getText();
        String age = txtAge.getText();
        String amount = txtAmountMonth.getText();
        if (!id.equals("") &&!name.equals("") && !mobile.equals("") && !email.equals("") && !fatherName.equals("") && !motherName.equals("") && !age.equals("") && !aadhar.equals("") && !amount.equals("")) {
            comboGender.setEnabled(true);
            comboGymTime.setEnabled(true);
            btnSave.setEnabled(true);
        } else {
            comboGender.setEnabled(false);
            comboGymTime.setEnabled(false);
            btnSave.setEnabled(false);
        }
    }

