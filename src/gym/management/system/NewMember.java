    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(txtId.getText());
        String name = txtName.getText();
        String mobile = txtMobileNumber.getText();
        String email = txtEmail.getText();
        String fatherName = txtFatherName.getText();
        String motherName = txtMotherName.getText();
        String aadhar = txtAadhar.getText();
        int age = Integer.parseInt(txtAge.getText());
        String amount = txtAmountMonth.getText();
        String gender = (String) comboGender.getSelectedItem();
        String gymTime = (String) comboGymTime.getSelectedItem();
        try {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            String query = "insert into members values (" + id + ", ' " + name + " ',' " + mobile + " ',' " + email + " ',' " + gender + " ',' " + fatherName + "',' " + motherName + "',' " + gymTime + "'," + age + ",'" + aadhar + "',' " + amount + " ')";
            st.execute(query);
            JOptionPane.showMessageDialog(null, "<html><b style=\"color:green;\">Successfully Updated!</b></html>");
            clear();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnSaveActionPerformed

